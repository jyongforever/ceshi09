# - 判断101-200之间有多少个素数，并输出所有素数。

#素数：这个数除以1余数=0，除以它本身余数也是0，除以1到它本身之间的数余数都不为0

# 遍历100-200，变量为i

# 判断i除以2到i之间的整数，余数是否为0，只要出现为0的，这个数就不是素数

i = 101
n = 0
while i <= 200:
    j = 2
    # k = 0
    while j < i: # 6%2 k = 1 ;6%3 k =2 ;6%4 k =2;6%5 k=2
        if i % j == 0:
            print("%d不是素数" % i)
            # k += 1
            break
        j += 1
    else:
        n += 1
        print("%d是素数"%i)

    i += 1
print("素数有%d个"%n)

